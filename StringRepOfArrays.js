/**
 * Problem - String Representations of Arrays
 * Note - when we try to print the array, it automatically calls the toString function for that array. 
 */

 var names = ["Mike", "John", "David"];
 var namesStr = names.join();
 console.log(namesStr); //Mike,John,David
 var namesStr1 = names.toString();
 console.log(namesStr1); //Mike,John,David